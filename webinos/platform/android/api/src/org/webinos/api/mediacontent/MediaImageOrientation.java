package org.webinos.api.mediacontent;

/**
 * Description: Defines orientation of a image. The meaning of available values
 * is illustrated, for example, at http://www.imagemagick.org/Usage/warping/.
 * 
 * Values: NORMAL, FLIP_HORIZONTAL, ROTATE_180, FLIP_VERTICAL, TRANSPOSE, ROTATE_90,
 * TRANSVERSE, ROTATE_270.
 * 
 * @author marius
 * 
 */
public enum MediaImageOrientation {
	NORMAL, FLIP_HORIZONTAL, ROTATE_180, FLIP_VERTICAL, TRANSPOSE, ROTATE_90, TRANSVERSE, ROTATE_270;
}
